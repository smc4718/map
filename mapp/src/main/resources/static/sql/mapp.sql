-- 테이블 삭제
DROP TABLE IF EXISTS COMPENSATION_T;
DROP TABLE IF EXISTS POINT_T;
DROP TABLE IF EXISTS ORDER_T;
DROP TABLE IF EXISTS NOTICE_IMAGE_T;
DROP TABLE IF EXISTS NOTICE_T;
DROP TABLE IF EXISTS REFERRAL_T;
DROP TABLE IF EXISTS USER_T;


-- 회원 테이블
CREATE TABLE USER_T (
    USER_NO			INT 			AUTO_INCREMENT PRIMARY KEY,
    ID 				VARCHAR(20) NOT NULL UNIQUE,
    PW 				VARCHAR(64) NOT NULL,
    NAME 			VARCHAR(50),
    GENDER 			VARCHAR(2),
    EMAIL 			VARCHAR(50),
    HP 				VARCHAR(20),
    REFERRAL_CODE VARCHAR(20) UNIQUE,
    ROLE 			VARCHAR(20),
    JOINED_AT 		DATETIME,
    GRADE 			VARCHAR(10) DEFAULT 'M1',
    MAPP_POINT 	INT 			DEFAULT 0
);

-- 공지
CREATE TABLE NOTICE_T (
    NOTICE_NO		INT 			 AUTO_INCREMENT PRIMARY KEY,
    USER_NO			INT 			 NOT NULL,
    TITLE 			VARCHAR(500) NOT NULL,
    CONTENTS 		TEXT,
    HIT 				INT 			 DEFAULT 0,
    CREATED_AT 	VARCHAR(30),
    FOREIGN KEY (USER_NO) REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

-- 공지 이미지
CREATE TABLE NOTICE_IMAGE_T (
    NOTICE_NO 			INT,
    IMAGE_PATH 		VARCHAR(100),
    FILESYSTEM_NAME 	VARCHAR(100),
    PRIMARY KEY (NOTICE_NO, FILESYSTEM_NAME),
    FOREIGN KEY (NOTICE_NO) REFERENCES NOTICE_T(NOTICE_NO) ON DELETE CASCADE
);

-- 추천 관계
CREATE TABLE REFERRAL_T (
    REFERRAL_NO INT 		 AUTO_INCREMENT PRIMARY KEY,
    REFERRER_NO INT 		 NOT NULL,
    REFERRED_NO INT 		 NOT NULL,
    CREATED_AT  DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (REFERRER_NO) REFERENCES USER_T(USER_NO) ON DELETE CASCADE,
    FOREIGN KEY (REFERRED_NO) REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

-- 주문
CREATE TABLE ORDER_T (
    ORDER_NO 		 INT 				AUTO_INCREMENT PRIMARY KEY,
    BUYER_USER_NO  INT 				NOT NULL,
    PRODUCT_NAME   VARCHAR(100),
    AMOUNT 		    INT 				NOT NULL,
    USED_COUPON_YN CHAR(1) 		DEFAULT 'N',
    ORDER_DATETIME DATETIME 		DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (BUYER_USER_NO) REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

-- 수당
CREATE TABLE COMPENSATION_T (
    COMPENSATION_NO  INT 		AUTO_INCREMENT PRIMARY KEY,
    RECEIVER_USER_NO INT 		NOT NULL,
    ORDER_NO 			INT 		NOT NULL,
    GENERATION 		INT,
    RATE 				DECIMAL(5,4),
    AMOUNT 				INT,
    PAID_AT 			DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (RECEIVER_USER_NO) REFERENCES USER_T(USER_NO),
    FOREIGN KEY (ORDER_NO) REFERENCES ORDER_T(ORDER_NO)
);

-- 포인트
CREATE TABLE POINT_T (
    POINT_NO 	INT 			AUTO_INCREMENT PRIMARY KEY,
    USER_NO 	INT 			NOT NULL,
    ORDER_NO 	INT 			NOT NULL,
    AMOUNT 		INT,
    REASON 		VARCHAR(100),
    CREATED_AT DATETIME 	DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (USER_NO) REFERENCES USER_T(USER_NO),
    FOREIGN KEY (ORDER_NO) REFERENCES ORDER_T(ORDER_NO)
);

